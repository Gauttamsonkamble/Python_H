#Logical Operators

c = True
d = False

print(c and c)
print(c and d)
print(d and c)
print(c and d)

print("**** For and Operators ")

print(True and True)
print(True and False)
print(False and True)
print(False and False)

print("**** For or Operators ")

print(True or True)
print(True or False)
print(False or True)
print(False or False)

print("****\n For Not Operators ")

print(not(True))
print(not(False))