
name = "Gauttam"

print(name[0:2]) # goes from 0 to 0-1 ie 0 to 1

print(name[2:-1]) # same as name [2:6]

name2 = "DeveloperCorners"

print(len(name2))

print(name2[0:10])

print(name2[0:10:2]) # skip n-1 character ie here (2-1) skip character

print(name2[0:10:3]) # skip 3-1 character means 2 character

print(name2[:4]) # replace the first empty number with 0 # name[0:4]

print(name2[1:]) # replace the second empty number with length # name[1:16]